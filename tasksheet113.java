public class tasksheet113 {

    public static void main (String[] args){
          
        int total = 1 + 2;
        System.out.println(total);

        total -= total - 1;
        System.out.println(total);

        total *= total * 2; 
        System.out.println(total);

        total /= total / 2;
        System.out.println(total);

        total += total + 8;
        System.out.println(total);

        total %= total % 7;
        System.out.println(total);

    }
}
